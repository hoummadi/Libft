#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	long i;

	i = nb;
	if (i < 0)
	{
		write(fd, "-", 1);
		i = i * (-1);
	}
	if (i > 9)
	{
		ft_putnbr_fd(i / 10, fd);
		ft_putnbr_fd(i % 10, fd);
	}
	else
	{
		i += 48;
		write(fd, &i, 1);
	}
}
